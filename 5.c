// Para descobrir qual interruptor controla sua devida lampada devemos seguir os seguintes passos:

// Ligar o primeiro interruptor e deixar o mesmo ligado para que a lampada permaneça acesa.
// Ligar o segundo interruptor, deixar durante um tempo ligado e logo desligar, deixando a lampada quente.
// Não ligar o terceiro interruptor.

// Após esses passos visitando apenas duas salas conseguimos ter a resposta, com as seguintes afirmações:

// lampada ligada = Interruptor 1
// lampada desligada porém quente = Interruptor 2
// Lampada desligada = Interruptor 3

// Logo indo a sala 1 e 2  teremos uma afirmação devido a logica.